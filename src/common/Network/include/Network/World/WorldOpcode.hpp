    constexpr std::string_view server_opcode_to_string(uint32_t op)
    {
        if (op == 0) return "NULL_OPCODE";
        for (auto const& e : SERVER_OPCODE_TABLE)
            if (e.value == op) return e.name;
        return "UNKNOWN_SMSG";
    }

    constexpr std::string_view client_opcode_to_string(uint32_t op)
    {
        if (op == 0) return "NULL_OPCODE";
        for (auto const& e : CLIENT_OPCODE_TABLE)
            if (e.value == op) return e.name;
        return "UNKNOWN_CMSG";
    }

